#!/bin/bash

red=$'\033[0;31m' 
lred=$'\033[1;31m'  
defa=$'\033[0m'     

function cecho (){
echo $(echo $1 | perl -pe "s/([q|p])/${lred}\1${defa}/g" | perl -pe "s/([\*|6\g])/${red}\1${defa}/g")
}

function get_oppai() {

case $1 in
  'A' )
cecho "Xd000000000000bXXd0000000000000bXX"
cecho "Xd0000000000000bXd0000000000000bXX"
cecho "d00000000000000bXd00000000000000bX"
cecho "d000ooo00000000bXd0000000ooo0000bX"
cecho "d000p**q0000000bXd0000000p**q000bX"
cecho "Xd0006g00000000bXd000000006g000bXX"
cecho "XXd0009WWWWWWWbXXXdWWWWWWW9000bXXX"
;;
   'B' )
cecho "XXd000000000000bXXXd000000000000bXXX"
cecho "Xd00000000000000bXd00000000000000bXX"
cecho "d000000000000000bXd000000000000000bX"
cecho "d000ooo000000000bXd00000000ooo0000bX"
cecho "d000p**q00000000bXd00000000p**q000bX"
cecho "Xd0006g000000000bXd0000000006g000bXX"
cecho "XXd0009WWWWWWWWbXXXdWWWWWWWW9000bXXX"
;;
   'C' )
cecho "XXd0000000000000bXXd0000000000000bXXX"
cecho "Xd000000000000000bd000000000000000bXX"
cecho "d0000000000000000bd0000000000000000bX"
cecho "d000ooo0000000000bd000000000ooo0000bX"
cecho "d000p**q000000000bd000000000p**q000bX"
cecho "Xd0006g000000000bXXd0000000006g000bXX"
cecho "XXd0009WWWWWWWWbXXXXdWWWWWWWW9000bXXX"
;;
  'D' )
cecho "XXd0000000000000bXXXXd0000000000000bXXX"
cecho "Xd000000000000000bXXd000000000000000bXX"
cecho "d00000000000000000bd00000000000000000bX" 
cecho "d000ooo00000000000bd0000000000ooo0000bX"
cecho "d000p**q0000000000bd0000000000p**q000bX"
cecho "Xd0006g0000000000bXXd00000000006g000bXX"
cecho "XXd009WWWWWWWWWWbXXXXdWWWWWWWWWW900bXXX"
;;
  'E' )
cecho "XXd0000000000000bXXXXd0000000000000bXXX"
cecho "Xd000000000000000bXXd000000000000000bXX"
cecho "Xd0000000000000000bXd0000000000000000bX"
cecho "d00000000000000000bd00000000000000000bX"
cecho "d000ooo00000000000bd0000000000ooo0000bX"
cecho "d000p**q0000000000bd0000000000p**q000bX"
cecho "Xd0006g0000000000bXXd00000000006g000bXX"
cecho "XXd009WWWWWWWWWWbXXXXdWWWWWWWWWW900bXXX"
;;
  'F' )
cecho "XXd00000000000000bXXXXd00000000000000bXXX"
cecho "Xd0000000000000000bXXd0000000000000000bXX"
cecho "Xd00000000000000000bXd00000000000000000bX"
cecho "d00000000000000000000000000000000000000bX"
cecho "d000ooo0000000000000000000000000ooo0000bX"
cecho "d000p**q000000000000000000000000p**q000bX"
cecho "Xd0006g000000000000bd0000000000006g000bXX"
cecho "XXd009WWWWWWWWWWWWbXXdWWWWWWWWWWWW900bXXX"
;;
  'G' )
cecho "XXd00000000000000bXXXXd00000000000000bXXX"
cecho "Xd0000000000000000bXXd0000000000000000bXX"
cecho "Xd000000000000000000b000000000000000000bX"
cecho "d00000000000000000000000000000000000000bX"
cecho "d000ooo0000000000000000000000000ooo0000bX"
cecho "d000p**q000000000000000000000000p**q000bX"
cecho "Xd0006g0000000000000d0000000000006g000bXX"
cecho "Xd000000000000000000d00000000000000000bXX"
cecho "XXd009WWWWWWWWWWWwWWdWWWWWWWWWWWWW900bXXX"
;;

  * )
echo $1カップは未対応
esac
}
while :
do
   read -s -n 1 cup
   clear
   if [ ! -z $(echo $cup | grep "[a-z|A-Z]") ];
   then
     cup=`echo $cup| tr "a-z" "A-Z"`
     size=$(expr $(printf "%d" \'$cup) - 60 ) 
     get_oppai $cup
     continue
   else
     break
   fi
done
